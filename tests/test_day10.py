import day10


def test_small_1():
    with open("input_10_small.txt") as raw:
        inpt = day10.parse_1(raw)
        assert day10.day10_1(inpt) == 13140


def test_place_sprite():
    day10.place_sprite(1) == "###....................................."
    day10.place_sprite(16) == "...............###......................"
    day10.place_sprite(5) == "....###................................."
    day10.place_sprite(11) == "..........###..........................."
    day10.place_sprite(8) == ".......###.............................."


